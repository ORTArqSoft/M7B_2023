const v8 = require("node:v8");

const heapStatistics = v8.getHeapStatistics();

console.log("Heap Statistics:");
console.log(
  "Used Heap Size:",
  heapStatistics.used_heap_size / (1024 * 1024),
  "MB"
);
console.log(
  "Total Heap Size:",
  heapStatistics.total_heap_size / (1024 * 1024),
  "MB"
);
console.log(
  "Heap Size Limit:",
  heapStatistics.heap_size_limit / (1024 * 1024),
  "MB"
);
console.log(
  "Total Available Size:",
  heapStatistics.total_available_size / (1024 * 1024),
  "MB"
);
